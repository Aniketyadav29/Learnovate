public class VariableDemo1 {
    public static void main(String[] args) {
        // Declare first, then initialize
        int num;
        num = 10;
        System.out.println("Number after initialization: " + num);

        // Declare + Initialize in one step
        int value = 20;
        System.out.println("Declared + Initialized: " + value);
    }
}
