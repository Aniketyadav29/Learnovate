public class QuestionNum4 {
    public static void main(String[] args) {
        // Pre-increment
        int a = 10;
        System.out.println("Pre-increment (a=10): " + (++a));

        // Post-increment
        int b = 10;
        System.out.println("Post-increment (b=10): " + (b++));
        System.out.println("Final value of b: " + b);
    }
}
