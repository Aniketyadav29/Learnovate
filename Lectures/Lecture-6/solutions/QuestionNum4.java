public class QuestionNum4 {
    public static void main(String[] args) {
        int sum = 0; // Initialize sum variable
        
        // For loop to calculate sum of numbers from 1 to 100
        for (int i = 1; i <= 100; i++) {
            sum += i; // Add current number to sum
        }
        
        System.out.println("Sum of numbers from 1 to 100: " + sum); // Print result
    }
}
