public class QuestionNum1 {
    public static void main(String[] args) {
        int number = -5; // Predefined number to check
        
        // If-else statement to check if number is positive, negative, or zero
        if (number > 0) {
            System.out.println("Positive"); // Executes if number is greater than 0
        } else if (number < 0) {
            System.out.println("Negative"); // Executes if number is less than 0
        } else {
            System.out.println("Zero"); // Executes if number equals 0
        }
    }
}
