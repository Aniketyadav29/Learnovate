public class VariableDemo2 {
    public static void main(String[] args) {
        // Direct initialization
        int a = 5;

        // Using another variable
        int b = a;

        // Using an expression
        int c = a + b;

        System.out.println("a = " + a);
        System.out.println("b = " + b);
        System.out.println("c = " + c);
    }
}
